package com.chiru.designpatterns.patterns.builder;

public class Meal {
    private String curry;
    private String biryani;
    private String coolDrink;
    public String getCurry() {
        return curry;
    }
    public void setCurry(String curry) {
        this.curry = curry;
    }
    public String getBiryani() {
        return biryani;
    }
    public void setBiryani(String biryani) {
        this.biryani = biryani;
    }
    public String getCoolDrink() {
        return coolDrink;
    }
    public void setCoolDrink(String coolDrink) {
        this.coolDrink = coolDrink;
    }

    
}
